package realboxhead;

public class Player {

	int x;
	int y;
	int health;
	int speed;
	int width;
	int height;
	boolean isCollide;

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}
	
	public int getHP() {
		return health;
	}
	
	public int getSpd() {
		return speed;
	}
	
	

}
